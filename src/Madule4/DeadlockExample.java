package Madule4;

public class DeadlockExample {
    private static final Object lock1 = new Object();
    private static final Object lock2 = new Object();

    public static void main(String[] args) {
        Thread t1 = new Thread(() -> {
            synchronized (lock1) {
                System.out.println("Поток 1: получил lock1");
                try { Thread.sleep(100); } catch (InterruptedException e) { }
                synchronized (lock2) {
                    System.out.println("Поток 1: получил lock2");
                }
            }
        });

        Thread t2 = new Thread(() -> {
            synchronized (lock2) {
                System.out.println("Поток 2: получил lock2");
                try { Thread.sleep(100); } catch (InterruptedException e) { }
                synchronized (lock1) {
                    System.out.println("Поток 2: получил lock1");
                }
            }
        });

        t1.start();
        t2.start();
    }
}
